function generateDataset() {
    const numUsers = 100;
    const numItems = ITEMS.length;
    const data = [];
    const profiles = [
        { name: "سالم‌خوار", highIdx: [0,1,2,3,4,10,11,12,13,14,20,21,29,30,43] },
        { name: "فست‌فودخور", highIdx: [32,33,38,39,40,41,46,47,48,49,27,28] },
        { name: "سرآشپز حرفه‌ای", highIdx: [15,16,17,18,22,23,24,25,26,31,34,35,36,37,42] },
        { name: "گیاه‌خوار", highIdx: [0,1,2,10,11,12,13,14,19,20,21,29,30,34,35,36] }
    ];
    for (let u = 0; u < numUsers; u++) {
        const profile = profiles[Math.floor(Math.random() * profiles.length)];
        const row = [];
        for (let i = 0; i < numItems; i++) {
            let base = 2.5;
            if (profile.highIdx.includes(i)) base = 4.0 + Math.random() * 0.8;
            else base = 1.5 + Math.random() * 2.0;
            let rating = Math.round(Math.min(5, Math.max(1, base + (Math.random() - 0.5) * 1.2)));
            row.push(rating);
        }
        data.push(row);
    }
    return data;
}

const USER_RATINGS = generateDataset();